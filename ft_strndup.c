#include "ft_print.h"

char	*ft_strndup(int n, char *s1)
{
	int		i;
	char	*s2;

	i = 0;
	if (!(s2 = ft_strnew(n)))
		return (NULL);
	while (s1[i] && i < n)
	{
		s2[i] = s1[i];
		i++;
	}
	return (s2);
}
